#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*dup;
	unsigned int	len;
	unsigned int	i;

	len = ft_strlen(s1);
	dup = (char *)malloc((len + 1) * sizeof(char));
	if (!dup)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
